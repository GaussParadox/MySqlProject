    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed
        try{
            Connection conexion = conectar.conectar();
            String id = buscar.getText().trim();
            PreparedStatement editar=conexion.prepareStatement("UPDATE estudiantes SET nombre = ?, telefono = ?, carrera = ? WHERE id = " +id);
            editar.setString(1,nombre.getText().trim());
            editar.setString(2,telefono.getText().trim());
            editar.setString(3,carrera.getText().trim());
            editar.executeUpdate();
            
            JOptionPane.showMessageDialog(null,"Edicion hecha . ");
            
            conectar.cerrarConexion();
            
        }catch(Exception error){
            JOptionPane.showMessageDialog(null,"Eror : "+error);
        }
    }//GEN-LAST:event_editarActionPerformed

    private void buscar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscar2ActionPerformed
       try {
    Connection conexion = conectar.conectar();
    
    PreparedStatement buscar3 = conexion.prepareStatement("SELECT * FROM estudiantes WHERE id=?");
    buscar3.setString(1, buscar.getText().trim());
    ResultSet consulta = buscar3.executeQuery();
    
    boolean datosEncontrados = false; // Variable para verificar si se encontraron datos
    
    while (consulta.next()) {
        nombre.setText(consulta.getString("nombre"));
        telefono.setText(consulta.getString("telefono"));
        carrera.setText(consulta.getString("carrera"));
        datosEncontrados = true; // Se encontraron datos
    }
    
    if (!datosEncontrados) {
        JOptionPane.showMessageDialog(null, "No se han encontrado datos.");
    } else {
        JOptionPane.showMessageDialog(null, "Datos encontrados.");
    }
    
    conectar.cerrarConexion();
} catch (Exception error) {
    System.out.println(error);
}
    }//GEN-LAST:event_buscar2ActionPerformed

