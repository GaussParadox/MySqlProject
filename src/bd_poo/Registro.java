    private void BotonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonRegistrarActionPerformed
        try{
            Connection conexion = conectar.conectar();
            String telefonoStr = telefono.getText().trim();
            
        if (telefonoStr.length() != 10) {
            JOptionPane.showMessageDialog(null, "El número de teléfono debe tener exactamente 10 dígitos.");
            return; 
        }
        
            PreparedStatement insertar = conexion.prepareStatement("Insert into estudiantes values(?,?,?,?)");
            
            insertar.setString(1, "0");
            insertar.setString(2, nombre.getText().trim());
            insertar.setString(3, telefono.getText().trim());
            insertar.setString(4, carrera.getText().trim());
            insertar.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Datos Registrados .");
            
            conectar.cerrarConexion();
            
        }catch(Exception error){
            JOptionPane.showMessageDialog(null,error);
        }
    }//GEN-LAST:event_BotonRegistrarActionPerformed

    private void consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarActionPerformed
        
        informacion.setText("");
        
        try{
            Connection conexion = conectar.conectar();
            PreparedStatement seleccionar = conexion.prepareStatement("SELECT * FROM estudiantes");
            ResultSet consulta=seleccionar.executeQuery();
            
           while (consulta.next()) {
        
        for (int i = 1; i <= consulta.getMetaData().getColumnCount(); i++) {
            informacion.append(consulta.getString(i));
            informacion.append("    ");
        }
        informacion.append("\n"); // Salto de línea
    }
           conectar.cerrarConexion();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error: " +e);
        }
    }//GEN-LAST:event_consultarActionPerformed

